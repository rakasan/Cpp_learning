/* Check if a number is even or odd */
#include<iostream>
using namespace std;

int main()
{
    int num;
    cout << "enter the number ";
    cin >>num;

    if(num % 2 == 0)
    {
        cout <<"The number is even";
    }
    else
    {
        cout << "The number is even";
    }

    return 0;

}
